class unaryoperators {
    public static void main(String[] args) {
       int s = 0;
       System.out.println(s);
       s = +1;//'+' operator.
       System.out.println(s);
       s = -s;//'-' operator.
       System.out.println(s);
       s++;//'++' operator.
       System.out.println(s);
       s--;//'--' operator.
       System.out.println(s);
       int b = 20;//'~' operator.
       System.out.println(~b);
       boolean x = true;//'!' operator.
       System.out.println(!x);


    }
}
