public class shiftoperators {
    public static void main(String[] args) {
        System.out.println(-10<<2);
        System.out.println(-20<<2);
        System.out.println(20>>2);
        System.out.println(-20>>2);
        System.out.println(-20>>>2);
        System.out.println(20>>>2);
        System.out.println(2^3);
    }
}
